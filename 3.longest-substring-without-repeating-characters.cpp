class Solution {
public:
    int lengthOfLongestSubstring(string s) {
        vector<int> pos(128, -1);
        int left = 0;
        int maxLen = 0;
        for (int i = 0; i < s.size();i++){
            if(pos[s[i]]>=left){
                left =  pos[s[i]]+1;
            }
            pos[s[i]] = i;
            maxLen = max(maxLen, i - left+1);
        }
        return maxLen;
    }
};
